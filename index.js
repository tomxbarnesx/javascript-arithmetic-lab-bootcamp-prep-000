function add(a, b){
  return a + b;
}

function subtract(a, b){
  return a - b;
}

function multiply(a, b){
  return a * b;
}

function divide(a, b){
  return a / b;
}

function inc(n){
  n++;
}

function dec(n){
  return n--;
}

function makeInt(n){
  parseInt(n);
}

function preserveDecimal(n){
  parseFloat(n);
}